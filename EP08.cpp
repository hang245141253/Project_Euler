#include <cstdio>

int ctoi(char ch) {
    return ch - '0';
}

// 暴力循环
long long process(char *arr) {
    int wid[13];
    long long max = 1;
    for (int i = 0; i <= 987; i++) {
        long long fuck;
        for (long long j = 0, mul = 1; j < 13; j++) {
            wid[j] = ctoi(arr[i + j]);
            mul *= wid[j];
            fuck = mul;
        }
        if (max < fuck) max = fuck;
    }
    return max;
}

// 滑动窗口
long long slidingWindow(char *arr) {
    long long ans = 0, mul = 1, zero = 0;
    for (int i = 0; arr[i]; i++) {
        arr[i] -= '0'; // 转数字

        // 进值
        if (arr[i])     mul *= arr[i];    
        else            zero++; // 单独判断0值，不能乘除，否则会清空原数据

        // 出值
        if (i < 13) continue;   // 收录前13个值
        if (arr[i - 13])    mul /= arr[i - 13];
        else                zero--;

        // 判断最大值
        if (!zero && ans < mul)  ans = mul; // zero有零则绝对不会是最大值，跳过
    }
    return ans;
}

